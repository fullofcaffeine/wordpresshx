function modifiersOf(node) {
  return new Set((node.modifiers ?? []).map((modifier) => modifier.kind));
}

function nameText(node, sourceFile) {
  return node.name?.getText(sourceFile) ?? "constructor";
}

function parametersOf(node, sourceFile) {
  return node.parameters.map((parameter) => ({
    name: parameter.name.getText(sourceFile),
    type: parameter.type?.getText(sourceFile) ?? null,
    optional: Boolean(parameter.questionToken || parameter.initializer)
  }));
}

function weakTypeInventory(node, sourceFile) {
  const inventory = { authoredPublicAny: 0, authoredPublicUnknown: 0 };
  const visit = (child) => {
    if (child.kind === ts.SyntaxKind.AnyKeyword) {
      inventory.authoredPublicAny += 1;
    }
    if (child.kind === ts.SyntaxKind.UnknownKeyword) {
      inventory.authoredPublicUnknown += 1;
    }
    ts.forEachChild(child, visit);
  };
  visit(node);
  return inventory;
}

function addInventory(target, addition) {
  target.authoredPublicAny += addition.authoredPublicAny;
  target.authoredPublicUnknown += addition.authoredPublicUnknown;
}

function extractPublicContract(file) {
  const sourceText = fs.readFileSync(file, "utf8");
  const sourceFile = ts.createSourceFile(
    file,
    sourceText,
    ts.ScriptTarget.Latest,
    true,
    file.endsWith(".tsx") ? ts.ScriptKind.TSX : ts.ScriptKind.TS
  );
  const classNode = sourceFile.statements.find(
    (statement) =>
      ts.isClassDeclaration(statement) &&
      statement.name?.text === expected.publicContract.className
  );
  assert.ok(classNode, `missing ${expected.publicContract.className} in ${file}`);

  const expectedMethodNames = new Set(
    expected.publicContract.methods.map((method) => method.name)
  );
  const methods = [];
  const inventory = { authoredPublicAny: 0, authoredPublicUnknown: 0 };
  for (const member of classNode.members) {
    const memberName = nameText(member, sourceFile);
    if (!expectedMethodNames.has(memberName)) {
      continue;
    }
    assert.ok(ts.isMethodDeclaration(member), `${memberName} is not a method`);
    assert.ok(
      modifiersOf(member).has(ts.SyntaxKind.StaticKeyword),
      `${memberName} is not static`
    );
    methods.push({
      kind: "static-method",
      name: memberName,
      typeParameters: (member.typeParameters ?? []).map((typeParameter) =>
        typeParameter.name.getText(sourceFile)
      ),
      parameters: parametersOf(member, sourceFile),
      returns: member.type?.getText(sourceFile) ?? null
    });
    addInventory(inventory, weakTypeInventory(member, sourceFile));
  }
  methods.sort((left, right) => left.name.localeCompare(right.name));

  const typeAliases = {};
  for (const aliasName of Object.keys(expected.publicContract.typeAliases).sort()) {
    const alias = sourceFile.statements.find(
      (statement) =>
        ts.isTypeAliasDeclaration(statement) && statement.name.text === aliasName
    );
    assert.ok(alias, `missing ${aliasName} in ${file}`);
    assert.ok(ts.isTypeLiteralNode(alias.type), `${aliasName} is not a type literal`);
    typeAliases[aliasName] = Object.fromEntries(
      alias.type.members
        .filter(ts.isPropertySignature)
        .map((member) => {
          assert.ok(!member.questionToken, `${aliasName} has an optional member`);
          return [
            member.name.getText(sourceFile),
            member.type?.getText(sourceFile) ?? null
          ];
        })
        .sort(([left], [right]) => left.localeCompare(right))
    );
    addInventory(inventory, weakTypeInventory(alias.type, sourceFile));
  }

  assert.equal(methods.length, expectedMethodNames.size);
  return { methods, typeAliases, weakTypeInventory: inventory };
}

