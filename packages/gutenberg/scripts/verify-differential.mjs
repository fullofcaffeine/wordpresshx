function walkFiles(root) {
  const files = [];
  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files.sort();
}

function relativeTree(root) {
  return new Map(
    walkFiles(root).map((file) => [
      path.relative(root, file).split(path.sep).join("/"),
      fs.readFileSync(file)
    ])
  );
}

function treeDigest(root) {
  const hash = createHash("sha256");
  for (const [relativePath, content] of relativeTree(root)) {
    hash.update(relativePath);
    hash.update("\0");
    hash.update(content);
    hash.update("\0");
  }
  return hash.digest("hex");
}

function assertSameTree(firstRoot, secondRoot) {
  const first = relativeTree(firstRoot);
  const second = relativeTree(secondRoot);
  assert.deepEqual([...first.keys()], [...second.keys()], "generated file set drift");
  for (const [relativePath, firstContent] of first) {
    assert.deepEqual(
      firstContent,
      second.get(relativePath),
      `generated bytes drifted for ${relativePath}`
    );
  }
}

