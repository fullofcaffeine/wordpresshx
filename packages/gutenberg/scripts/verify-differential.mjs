async function bundleLane(root, lane, extension) {
  const laneRoot = path.join(root, lane);
  const runtimeRoot = path.join(laneRoot, "runtime");
  fs.mkdirSync(runtimeRoot, { recursive: true });
  const outfile = path.join(runtimeRoot, "bundle.mjs");
  await build({
    absWorkingDir: laneRoot,
    entryPoints: [`index.${extension}`],
    outfile,
    bundle: true,
    format: "esm",
    jsx: "automatic",
    jsxImportSource: "react",
    platform: "node",
    target: ["node22"],
    packages: "external",
    treeShaking: true,
    minify: false,
    sourcemap: false,
    logLevel: "silent"
  });
  const bytes = fs.readFileSync(outfile);
  return {
    outfile,
    bytes: bytes.length,
    sha256: createHash("sha256").update(bytes).digest("hex")
  };
}

