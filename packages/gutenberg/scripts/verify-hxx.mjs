function installDomGlobals(window) {
  for (const name of [
    "CSSStyleSheet",
    "DocumentFragment",
    "Element",
    "Event",
    "HTMLElement",
    "HTMLButtonElement",
    "KeyboardEvent",
    "MouseEvent",
    "MutationObserver",
    "Node",
    "NodeFilter",
    "SVGElement",
  ]) {
    if (name in window) {
      Object.defineProperty(globalThis, name, {
        configurable: true,
        value: window[name],
      });
    }
  }
  Object.defineProperties(globalThis, {
    document: { configurable: true, value: window.document },
    getComputedStyle: { configurable: true, value: window.getComputedStyle.bind(window) },
    navigator: { configurable: true, value: window.navigator },
    window: { configurable: true, value: window },
  });
  const matchMedia = (query) => ({
    addEventListener() {},
    addListener() {},
    dispatchEvent() {
      return true;
    },
    matches: false,
    media: query,
    onchange: null,
    removeEventListener() {},
    removeListener() {},
  });
  class ResizeObserver {
    disconnect() {}
    observe() {}
    unobserve() {}
  }
  class IntersectionObserver extends ResizeObserver {
    takeRecords() {
      return [];
    }
  }
  window.matchMedia = matchMedia;
  window.ResizeObserver = ResizeObserver;
  window.IntersectionObserver = IntersectionObserver;
  Object.defineProperties(globalThis, {
    IntersectionObserver: { configurable: true, value: IntersectionObserver },
    IS_REACT_ACT_ENVIRONMENT: { configurable: true, value: true },
    ResizeObserver: { configurable: true, value: ResizeObserver },
    cancelAnimationFrame: {
      configurable: true,
      value: (handle) => clearTimeout(handle),
    },
    requestAnimationFrame: {
      configurable: true,
      value: (callback) => setTimeout(() => callback(Date.now()), 0),
    },
  });
  window.cancelAnimationFrame = globalThis.cancelAnimationFrame;
  window.requestAnimationFrame = globalThis.requestAnimationFrame;
  window.scrollTo = () => {};
}

