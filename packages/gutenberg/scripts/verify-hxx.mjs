function weakKeyword(node) {
  if (node.kind === ts.SyntaxKind.AnyKeyword) {
    return "any";
  }
  if (node.kind === ts.SyntaxKind.UnknownKeyword) {
    return "unknown";
  }
  return null;
}

const publicWeakTypes = [];
function inspectTypeTree(root, label) {
  if (!root) {
    return;
  }
  function visit(node) {
    const weak = weakKeyword(node);
    if (weak) {
      publicWeakTypes.push(`${label}:${weak}`);
    }
    ts.forEachChild(node, visit);
  }
  visit(root);
}
for (const statement of mainSourceFile.statements) {
  if (ts.isClassDeclaration(statement)) {
    for (const member of statement.members) {
      const name = member.name?.getText(mainSourceFile) ?? "constructor";
      inspectTypeTree(member.type, `Main.${name}`);
      for (const parameter of member.parameters ?? []) {
        inspectTypeTree(parameter.type, `Main.${name}.${parameter.name.getText()}`);
      }
    }
  } else if (ts.isTypeAliasDeclaration(statement)) {
    inspectTypeTree(statement.type, statement.name.text);
  }
}
assert.deepEqual(publicWeakTypes, [], "weak type leaked into fixture API");

const internalWeakInventory = [];
function visitInternalWeak(node, owner = "module") {
  let nextOwner = owner;
  if (ts.isMethodDeclaration(node)) {
    nextOwner = `Main.${node.name.getText(mainSourceFile)}`;
  }
  if (ts.isVariableDeclaration(node) && node.type) {
    const isAnyArray =
      ts.isArrayTypeNode(node.type) &&
      node.type.elementType.kind === ts.SyntaxKind.AnyKeyword;
    if (isAnyArray) {
      internalWeakInventory.push(
        `${owner}:${node.name.getText(mainSourceFile)}:any[]`
      );
    }
  }
  ts.forEachChild(node, (child) => visitInternalWeak(child, nextOwner));
}
