function nameText(node, sourceFile) {
  return node.name?.getText(sourceFile) ?? "constructor";
}

function parametersOf(node, sourceFile) {
  return node.parameters.map((parameter) => ({
    name: parameter.name.getText(sourceFile),
    type: parameter.type?.getText(sourceFile) ?? null,
    optional: Boolean(parameter.questionToken || parameter.initializer)
  }));
}

function callableContract(kind, name, node, sourceFile) {
  return {
    kind,
    name,
    typeParameters: (node.typeParameters ?? []).map((typeParameter) =>
      typeParameter.name.getText(sourceFile)
    ),
    parameters: parametersOf(node, sourceFile),
    returns: kind === "constructor" ? null : node.type?.getText(sourceFile) ?? null
  };
}

function extractPublicContract(file) {
  const sourceText = fs.readFileSync(file, "utf8");
  const sourceFile = ts.createSourceFile(
    file,
    sourceText,
    ts.ScriptTarget.Latest,
    true,
    file.endsWith(".d.ts") ? ts.ScriptKind.TS : ts.ScriptKind.TS
  );
  const classNode = sourceFile.statements.find(
    (statement) =>
      ts.isClassDeclaration(statement) &&
      statement.name?.text === expected.publicContract.className
  );
  assert.ok(classNode, `missing ${expected.publicContract.className} in ${file}`);

  const expectedNames = new Set(
    expected.publicContract.members.map((member) => member.name)
  );
  const members = [];
  const weakTypeNodes = [];
  for (const member of classNode.members) {
    const memberName = nameText(member, sourceFile);
    if (!expectedNames.has(memberName)) {
      continue;
    }
    if (ts.isConstructorDeclaration(member)) {
      members.push(callableContract("constructor", "constructor", member, sourceFile));
    } else if (ts.isPropertyDeclaration(member)) {
      members.push({
        kind: "property",
        name: memberName,
        type: member.type?.getText(sourceFile) ?? null
      });
    } else if (ts.isMethodDeclaration(member)) {
      members.push(callableContract("method", memberName, member, sourceFile));
    }
    const visit = (node) => {
      if (
        node.kind === ts.SyntaxKind.AnyKeyword ||
        node.kind === ts.SyntaxKind.UnknownKeyword
      ) {
        weakTypeNodes.push(node.getText(sourceFile));
      }
      ts.forEachChild(node, visit);
    };
    visit(member);
  }
  members.sort((left, right) => left.name.localeCompare(right.name));

  const signalAlias = sourceFile.statements.find(
    (statement) =>
      ts.isTypeAliasDeclaration(statement) && statement.name.text === "SignalSnapshot"
  );
  assert.ok(signalAlias && ts.isTypeLiteralNode(signalAlias.type));
  const signalSnapshot = Object.fromEntries(
    signalAlias.type.members
      .filter(ts.isPropertySignature)
      .map((member) => [
        member.name.getText(sourceFile),
        member.type?.getText(sourceFile) ?? null
      ])
      .sort(([left], [right]) => left.localeCompare(right))
  );
  const signalWeakNodes = [];
  const visitSignal = (node) => {
    if (
      node.kind === ts.SyntaxKind.AnyKeyword ||
      node.kind === ts.SyntaxKind.UnknownKeyword
    ) {
      signalWeakNodes.push(node.getText(sourceFile));
    }
    ts.forEachChild(node, visitSignal);
  };
  visitSignal(signalAlias.type);

  assert.doesNotMatch(sourceText, /\bimplementationDetail\b/);
  return {
    members,
    signalSnapshot,
    authoredPublicAny: [...weakTypeNodes, ...signalWeakNodes].filter(
      (value) => value === "any"
    ).length,
    authoredPublicUnknown: [...weakTypeNodes, ...signalWeakNodes].filter(
      (value) => value === "unknown"
    ).length
  };
}

