function verifyExportManifest(lane, compilerProfile) {
  const manifest = readJson(path.join(workRoot, lane, "browser-exports.json"));
  assert.deepEqual(
    Object.keys(manifest).sort(),
    ["compilerProfile", "entries", "profileId", "schemaVersion"]
  );
  assert.equal(manifest.schemaVersion, 1);
  assert.equal(manifest.profileId, expected.profileId);
  assert.equal(manifest.compilerProfile, compilerProfile);
  assert.equal(manifest.entries.length, 1);
  const entry = manifest.entries[0];
  assert.deepEqual(
    Object.keys(entry).sort(),
    [
      "exportName",
      "generatedModule",
      "haxeSource",
      "profileCapabilityRefs",
      "retentionRule",
      "sourceSpan",
      "stableExportId",
      "typeIdentity"
    ]
  );
  const { sourceSpan, ...plan } = entry;
  assert.deepEqual(plan, expected.exportPlan);
  assert.ok(Number.isInteger(sourceSpan.start));
  assert.ok(Number.isInteger(sourceSpan.end));
  assert.ok(sourceSpan.start >= 0 && sourceSpan.start < sourceSpan.end);
  const source = fs.readFileSync(path.join(packageRoot, entry.haxeSource));
  assert.ok(sourceSpan.end <= source.length, "export source span exceeds source");
  const selectedSource = source.subarray(sourceSpan.start, sourceSpan.end).toString();
  assert.match(selectedSource, /^class BrowserApi\s*\{/);
  assert.match(selectedSource, /public function identity<T>/);
  return manifest;
}

