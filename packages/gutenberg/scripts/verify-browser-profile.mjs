async function bundleLane(lane, extension) {
  const outfile = path.join(workRoot, lane, "bundle.mjs");
  const result = await build({
    absWorkingDir: path.join(workRoot, lane),
    entryPoints: [`src-gen/index.${extension}`],
    outfile,
    bundle: true,
    format: "esm",
    platform: "node",
    target: ["node22"],
    treeShaking: true,
    minify: false,
    sourcemap: false,
    metafile: true,
    logLevel: "silent"
  });
  const output = fs.readFileSync(outfile, "utf8");
  assert.doesNotMatch(output, /\bimplementationDetail\b/);
  assert.ok(result.metafile.outputs[outfile] || Object.keys(result.metafile.outputs).length === 1);
  return {
    outfile,
    bytes: Buffer.byteLength(output),
    sha256: createHash("sha256").update(output).digest("hex")
  };
}

