function formatDiagnostics(diagnostics) {
  return ts.formatDiagnosticsWithColorAndContext(diagnostics, {
    getCanonicalFileName: (fileName) => fileName,
    getCurrentDirectory: () => process.cwd(),
    getNewLine: () => "\n"
  });
}

function typecheckAndAudit(lane, sourcePredicate, entryRelativePath) {
  const laneRoot = path.join(workRoot, lane);
  const rootNames = walkFiles(laneRoot).filter(sourcePredicate);
  const program = ts.createProgram({ rootNames, options: compilerOptions });
  const diagnostics = ts.getPreEmitDiagnostics(program);
  assert.equal(diagnostics.length, 0, formatDiagnostics(diagnostics));

  const entry = program.getSourceFile(path.join(laneRoot, entryRelativePath));
  assert.ok(entry, `missing semantic entry source for ${lane}`);
  const moduleSymbol = program.getTypeChecker().getSymbolAtLocation(entry);
  assert.ok(moduleSymbol, `missing semantic module symbol for ${lane}`);
  const exports = program
    .getTypeChecker()
    .getExportsOfModule(moduleSymbol)
    .map((symbol) => symbol.getName())
    .sort();
  assert.deepEqual(exports, ["BrowserApi"]);
}

